
## Tipos de datos

"""
Python tiene 7 tipos de datos:

- int
- float
- str
- bool
- list
- dict
- complex

"""

# int
print(type(10))
print(type(10.0))
print(type(-5))
print(type(722342374327423874832743284738472384))

# float
print(type(10.0))
print(type(1.0))
print(type(-5.0))

# complex
print(type(10j))

# str
print(type("Hola Mundo"))
print(type("Python"))

# bool
print(type(True))
print(type(False))

# list
lista = [1, 2, 3, 4, 5]
print(type(lista))

# dict
diccionario = {"a": 1, "b": 2, "c": 3}
print(type(diccionario))

# None
print(type(None))